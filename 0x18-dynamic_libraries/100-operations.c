/**
 * add - adds two nums
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtracts two nums
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiplies two nums
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division of two nums
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus of two nums
 * @a: first num
 * @b: second num
 *
 * Return: result of operation
 */

int mod(int a, int b)
{
	return (a % b);
}
